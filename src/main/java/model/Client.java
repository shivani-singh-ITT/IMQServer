package model;

public class Client {
	private String Id;
	private String clientName;

	public void setId(String Id) {
		this.Id = Id;
	}

	public void setClientName(String clientName) {
		this.clientName = clientName;
	}

	public String getId() {
		return Id;
	}

	public String getClientName() {
		return clientName;
	}

}
